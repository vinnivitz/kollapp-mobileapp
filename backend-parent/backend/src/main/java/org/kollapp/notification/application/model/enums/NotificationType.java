package org.kollapp.notification.application.model.enums;

/**
 * Enum representing different types of push notifications.
 * Categories are designed from user perspective to allow granular notification preferences.
 */
public enum NotificationType {
    GENERAL,
    MEMBERSHIP_STATUS,
    MEMBERSHIP_CHANGES,
    ACTIVITIES,
    FINANCES,
    ANNOUNCEMENTS,
    MESSAGES,
    SYSTEM_ALERTS
}
